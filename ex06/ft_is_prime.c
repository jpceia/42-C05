int	ft_is_prime(int nb)
{
	long n;

	if (nb < 2)
		return (0);
	if (nb == 2)
		return (1);
	if (nb == 3)
		return (1);
	if (nb % 2 == 0)
		return (0);
	if (nb % 3 == 0)
		return (0);
	n = 1;
	while ((6 * n - 1) * (6 * n - 1) <= (long)nb)
	{
		if (nb % (6 * n - 1) == 0)
			return (0);
		if (nb % (6 * n + 1) == 0)
			return (0);
		n++;
	}
	return (1);
}
